void main() {
  String text = 'Test String'; //initialization
  print(text.toUpperCase());
  print(text.toLowerCase());
  print(text.length);

  String text2 = 'Test String 2'.toUpperCase(); //initialization
  print(text2);
  text2 = text2.toLowerCase(); //reassigning
  print(text2);
  print(text2.length);
}
